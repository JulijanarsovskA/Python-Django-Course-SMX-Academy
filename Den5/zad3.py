"""2. Da se napravat funkcii sobiranje, odzemanje, mnozenje, delenje. 
Broevite da bidat proizvolni. 
Da se pecatat celite operaacii zaedno so rezultatot
"""

def sobiranje():
    n1 = 6
    n2 = 3
    zbir = n1 + n2
    print("{} + {} = {}".format(n1, n2, zbir))

def odzemanje():
    n1 = 6
    n2 = 3
    razlika = n1 - n2
    print("{} - {} = {}".format(n1, n2, razlika))

def mnozenje():
    n1 = 6
    n2 = 3
    proizvod = n1 * n2
    print("{} * {} = {}".format(n1, n2, proizvod))  

def delenje():
    n1 = 6
    n2 = 3
    kolicnik = n1 / n2
    print("{} / {} = {}".format(n1, n2, kolicnik))  

sobiranje()
odzemanje()
mnozenje()
delenje()
